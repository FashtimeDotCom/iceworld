package v1.biz;

public class User {
	private String name;
	private String sex;
	private String grade;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name+"ss";
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}

}
